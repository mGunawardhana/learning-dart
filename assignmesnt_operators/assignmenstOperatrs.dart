void main() {
  var a,b;

  /** short hand addition */
  a = 20; b = 10;
  a += b; // 20 + 10 = 30 => a
  print(a); // 30

  /** short hand subtraction */
  a = 20; b = 10;
  a -= b; // 20 - 10 = 10 => a
  print(a); // 10

  /** short hand multiplication */
  a = 20; b = 10;
  a *= b; // 20 * 10 = 200 => a
  print(a);// 200

  /** short hand reminder */
  a = 20; b = 10;
  a %= b;// 20 % 10 = 0 => a
  print(a);// 0

  /** short hand division */
  a = 20; b = 10;
  a/=b;// 20 / 10 = 2 => a
  print(a);// 2

}
